package com.collectiongenerics.thiago.collections.map;

import java.util.HashMap;
import java.util.Map;

public class CollectionsHashMapExample {

	public static void main(String[] args) {
		Map<String,String> map = new HashMap();
		map.put("Adam", "adam@gmail.com");
		map.put("Kevin", "kevin@gmail.com");
		map.put("Anna", "anna@gmail.com");
		
		System.out.println(map.get("Adam"));
		
	
		
		
	}
	
}
